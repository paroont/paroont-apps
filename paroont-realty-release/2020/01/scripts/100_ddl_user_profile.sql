

-- ============================    USER USER_PROFILE ===========================
CREATE TABLE USER_PROFILE(
	PROFILE_ID	BIGINT NOT NULL AUTO_INCREMENT,
	USER_ID VARCHAR (255),
	FIRST_NAME	VARCHAR (255) NOT NULL,
	LAST_NAME	VARCHAR (255),
	MOBILE_NO	VARCHAR (255),
	MOBILE_COUNTRY_CODE	VARCHAR (255),
	EMAIL_ID	VARCHAR (255),
	STATUS_ID	INT NOT NULL DEFAULT 1,
	STATUS_TITLE	VARCHAR (255),
	PROFILE_TYPE_ID	INT NOT NULL DEFAULT 2,
	PROFILE_TYPE_TITLE	VARCHAR (255),
	COMPANY_NAME	VARCHAR (255),
	COMPANY_WEB_URL	VARCHAR (255),
	COMPANY_OVERVIEW	VARCHAR (2000),
	FACEBOOK_ID	VARCHAR (255),
	TWITTER_ID	VARCHAR (255),
	INSTAGRAM_ID	VARCHAR (255),
	WHATSAPP_NO	VARCHAR (255),
	WHATSAPP_COUNTRY_CODE	VARCHAR (255),
	RERA_ID	VARCHAR (255),
	EMAIL_ID_2	VARCHAR (255),
	EMAIL_ID_3	VARCHAR (255),
	CONTACT_NO_2	VARCHAR (255),
	CONTACT_NO_2_COUNTRY_CODE	VARCHAR (255),
	CONTACT_NO_2_TITLE	VARCHAR (255),
	CONTACT_NO_3	VARCHAR (255),
	CONTACT_NO_3_COUNTRY_CODE	VARCHAR (255),
	CONTACT_NO_3_TITLE	VARCHAR (255),
	ADDRESS_LINE_1	VARCHAR (255),
	ADDRESS_LINE_2	VARCHAR (255),
	CITY_ID	VARCHAR (255),
	CITY_NAME	VARCHAR (255),
	STATE_ID	VARCHAR (255),
	STATE_NAME	VARCHAR (255),
	LANDMARK_ID	VARCHAR (255),
	LANDMARK_NAME	VARCHAR (255),
	PIN_CODE	VARCHAR (255),
	COUNTRY_NAME	VARCHAR (255),
	GENDER_TITLE	VARCHAR (255),
	DOB	TIMESTAMP,
	INCOME_RANGE_ID	INT,
	INCOME_RANGE_TITLE	VARCHAR (255),
	CREATED_BY	VARCHAR (255) NOT NULL, 
	CREATED_TS	TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
	UPDATED_BY	VARCHAR (255) NOT NULL,
	UPDATED_TS	TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
	CONSTRAINT USER_PROFILE_PK_PROFILE_ID PRIMARY KEY(PROFILE_ID)
	);
	